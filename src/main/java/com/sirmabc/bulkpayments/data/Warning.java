package com.sirmabc.bulkpayments.data;

public class Warning {

    public String wcode;
    public String wdesc;

    public Warning() {
    }

    public String getWcode() {
        return wcode;
    }

    public void setWcode(String wcode) {
        this.wcode = wcode;
    }

    public String getWdesc() {
        return wdesc;
    }

    public void setWdesc(String wdesc) {
        this.wdesc = wdesc;
    }

}
